SELECT 
    T.ITEM_ID, I.ITEM_NAME
FROM ITEM_INFO AS I
    INNER JOIN 
    ITEM_TREE AS T
    USING(ITEM_ID)
WHERE T.PARENT_ITEM_ID IS NULL
ORDER BY T.ITEM_ID ASC;