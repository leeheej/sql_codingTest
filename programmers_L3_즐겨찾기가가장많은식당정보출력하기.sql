SELECT
    R.FOOD_TYPE, R.REST_ID, R.REST_NAME, R.FAVORITES
FROM REST_INFO AS R
    JOIN
    (SELECT
        FOOD_TYPE,
        MAX(FAVORITES) AS MAX_FAVORITES
    FROM REST_INFO
    GROUP BY FOOD_TYPE) AS M
    USING(FOOD_TYPE)
WHERE FAVORITES = MAX_FAVORITES
ORDER BY FOOD_TYPE DESC;

